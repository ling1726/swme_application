-- add a coutry id column to addresses
-- add foreign key to address table
ALTER TABLE ADDRESS ADD COLUMN COUNTRY_ID INTEGER;
ALTER TABLE ADDRESS ADD CONSTRAINT FK_COUNTRY FOREIGN KEY (COUNTRY_ID) REFERENCES COUNTRY(COUNTRY_PK);

