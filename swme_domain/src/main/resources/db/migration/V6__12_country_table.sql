-- create new COUNTRY table and its pk sequence
CREATE SEQUENCE SQ_COUNTRY_PK START 1;

CREATE TABLE COUNTRY(
  COUNTRY_PK INTEGER DEFAULT nextval('SQ_COUNTRY_PK'),
  CODE VARCHAR(2) NOT NULL,
  NAME VARCHAR(50) NOT NULL,
  CONSTRAINT C_COUNTRY_PK PRIMARY KEY(COUNTRY_PK)
